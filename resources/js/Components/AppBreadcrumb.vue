    name: 'AppBreadcrumb',
    setup() {
        const breadcrumbs = ref();

        const getBreadcrumbs = () => {
            return [];
        }

        onMounted(() => {
            breadcrumbs.value = getBreadcrumbs()
        })

        return {
            breadcrumbs,
        }
    },
}
</script>
